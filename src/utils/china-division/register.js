const filter = (code) => {
  let result = {};
  for (const key in DICT) {
    if (DICT.hasOwnProperty(key)) {
      const element = DICT[key];
      if (key.slice(0, 6) === code) {
        result[key] = element
      }
    }
  }
  return result;
}

const findKey = (obj, value, compare = (a, b) => a === b) =>{
  return Object.keys(obj).find(k => compare(obj[k], value))
}

/**
 * 根据code定位街道社区, 暂时code取中文
 * @param  {[type]} code [description]
 * @return {[type]}      [description]
 */
export const street_fixed = (name) => {
  const code = findKey(DICT, name);
  const FILTER_DICT = filter(code);
  let fixed = []
  for (let id in FILTER_DICT) {
    if (id.length >= 6 && {}.hasOwnProperty.call(FILTER_DICT, id)) {
      let pid
      const tmpObj = { id, value: FILTER_DICT[id], label: FILTER_DICT[id] }
      if (id.slice(6, 12) !== '') {
        pid =
          id.slice(9, 12) === ''
            ? `${id.slice(0, 6)}`
            : `${id.slice(0, 9)}`
      }
      if (pid) tmpObj.pid = pid
      fixed.push(tmpObj)
    }
  }
  let result = [];
  const res = tree(fixed);
  if (res && res[0] && res[0]['children']) {
    result = res[0]['children'];
  }

  return result;
}